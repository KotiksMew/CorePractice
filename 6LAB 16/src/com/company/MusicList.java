package com.company;

public class MusicList {
    private String artist;
    private String composer;
    private String discName;
    private String favoriteTrack;
    private String purchaseDate;
    private String discGiven;
    private Short tracksNumber;
    private Double duration;

    public String getArtist() {
        return artist;
    }

    public void setArtist(String artist) {
        this.artist = artist;
    }

    public String getComposer() {
        return composer;
    }

    public void setComposer(String composer) {
        this.composer = composer;
    }

    public String getDiscName() {
        return discName;
    }

    public void setDiscName(String discName) {
        this.discName = discName;
    }

    public String getFavoriteTrack() {
        return favoriteTrack;
    }

    public void setFavoriteTrack(String favoriteTrack) {
        this.favoriteTrack = favoriteTrack;
    }

    public String getPurchaseDate() {
        return purchaseDate;
    }

    public void setPurchaseDate(String purchaseDate) {
        this.purchaseDate = purchaseDate;
    }

    public String getDiscGiven() {
        return discGiven;
    }

    public void setDiscGiven(String discGiven) {
        this.discGiven = discGiven;
    }

    public Short getTracksNumber() {
        return tracksNumber;
    }

    public void setTracksNumber(Short tracksNumber) {
        this.tracksNumber = tracksNumber;
    }

    public Double getDuration() {
        return duration;
    }

    public void setDuration(Double duration) {
        this.duration = duration;
    }
}
